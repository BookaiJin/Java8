    public int countTriplets(int[] arr) {
        int a = 0;
        int b = 0;
        int sum = 0;
        for (int i = 0; i < arr.length; i++) {
            for (int j = i + 1; j < arr.length; j++) {
                for (int k = j; k < arr.length; k++) {
                    for (int z = i; z <= k; z++) {
                        if (z >= i && z <= j - 1) {
                            a ^= arr[z];
                        }
                        if (z >= j && z <= k) {
                            b ^= arr[z];
                        }
                    }
                    if (a == b) {
                        sum++;
                    }
                    a = 0;
                    b = 0;
                }
            }
        }
        return sum;
    }
